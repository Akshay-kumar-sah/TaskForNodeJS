const whois = require('whois');
const Domain = require('../Models/domainModel');

async function extractWhoisData(domain) {
  return new Promise((resolve, reject) => {
    whois.lookup(domain, (err, data) => {
      if (err) {
        reject(err);
      } else {
        
      // console.log({data,domain,err});
        resolve(data);
      }
    });
  });

}

async function parseWhoisData(whoisData) {
  // Same implementation as before

 const lines = whoisData.split('\n');
		const keyValuePairs = {};

		for (const line of lines) {
			const colonIndex = line.indexOf(':');
			if (colonIndex !== -1) {
				const key = line.substring(0, colonIndex).trim();
				const value = line.substring(colonIndex + 1).trim();
				keyValuePairs[key] = value;
      }
    }
    return keyValuePairs;


}

async function saveDomainInfo(domainInfo) {
  try {
    const domain = new Domain(domainInfo);
    await domain.save();
  } catch (error) {
    console.log(error);
    throw new Error('Error saving domain info to the database');
  }
}

